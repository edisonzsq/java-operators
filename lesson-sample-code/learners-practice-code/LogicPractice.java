class LogicPractice {

    public static void main(String[] args){

        /*
            Task 1: Resolve the OR operator to return false.

            - The expression that resolves to variable "a" is true. 
            - Modify the comparison operator so that "a" is false.
        */

        boolean a = (3 == 3) || (4 < 3); // Modify the comparison operator.

        System.out.println(a);

        
        /*
            Task 2: Resolve the AND operator to return true.

            - The expression that resolves to variable "b" is false. 
            - Modify the comparison operator so that "b" is true.
        */

        boolean b = (3 == 3) && (4 < 3); // Modify the comparison operator.
        System.out.println(b);
    }
}
